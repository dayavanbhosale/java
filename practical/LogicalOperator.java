package Dayavan;

public class LogicalOperator {

	  public static void main(String[] args) {
		   
		   
			//Variables Definition and Initialization
			  boolean a = true, b = false;
			  //Logical AND
			  System.out.println("a && b = " + (a && b));
			  
			  //Logical OR
			  System.out.println("a || b = " + (a || b) );
			  
			  //Logical Not
			  System.out.println("!(a && b) = " + !(a && b));
		}
		}

